    @Override
    public void mousePressed(MouseEvent e) {
        Celda celda = (Celda) e.getSource();

        if(SwingUtilities.isLeftMouseButton(e)){
            manejaClicIzquierdo(celda);
        }
        // PARA PRUEBAS
        else if(SwingUtilities.isRightMouseButton(e)){
            if(!celda.isEmpty()){
                celda.quitaUnidad();
                celda.repaint();
            }
            liberaEstadoCeldas();
        } 
        // PARA PRUEBAS FIN
        else if(SwingUtilities.isMiddleMouseButton(e)){
            if(celda.isEmpty())
                limpiaDatos();
            else
                actualizaDatosSelec(celda);
        } 
    }
    
    @Override
    public void mouseReleased(MouseEvent e) {
        // Sin usar de momento
        if(SwingUtilities.isMiddleMouseButton(e)){
            if(celdaSeleccionada != null)
                actualizaDatosSelec(celdaSeleccionada);
            else
                limpiaDatos();
        }
    }

    @Override
    public void mouseEntered(MouseEvent e) {
        Celda celda = (Celda) e.getSource();
        if(!celda.isSelected() && !celda.isMarcada()){
            celda.setBorder(bordeRaton);
            
        }
        if(SwingUtilities.isMiddleMouseButton(e)){
            if(celda.isEmpty())
                limpiaDatos();
            else
                actualizaDatosSelec(celda);
        }
        celda.oscurece();
    }

    @Override
    public void mouseExited(MouseEvent e) {
        Celda celda = (Celda) e.getSource(); 
        if(!celda.isSelected() && !celda.isMarcada()){
            celda.setBorder(bordeNormal);
        }
        celda.aclara();
    }
